// 安装cnpm(从淘宝源安装)
// npm install -g cnpm --registry=https://registry.npmmirror.com

// 配置npm镜像
// npm config set registry https://registry.npmmirror.com


// cnpm 的使用方法与 npm 基本相同，只需将 npm 命令替换为 cnpm 即可。